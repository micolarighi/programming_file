public class MOBA_HERO {

    String name;
    int health;
    int attack;
    int mana;
    String skill1;
    String skill2;
    String skill3;

    MOBA_HERO(String name, int health, int mana, int attack, String skill1, String skill2, String skill3) {
        this.name = name;
        this.health = health;
        this.attack = attack;
        this.mana = mana;
        this.skill1 = skill1;
        this.skill2 = skill2;
        this.skill3 = skill3;
    }
}